std::vector<std::string> textBuffer;

void draw_background(pico_ssd1306::SSD1306* display) {
    pico_ssd1306::drawText(display, font_5x8, VERSION, 0, 0);
    display->addBitmapImage(0, 0, 128, 64, image);
}

void cls(pico_ssd1306::SSD1306* display) {
	display->clear();

    draw_background(display);
    
    display->sendBuffer();
}

void printout(pico_ssd1306::SSD1306* display, const char* str) {
    // 1. Add new message to the buffer
    textBuffer.push_back(std::string(str));

    // 2. If buffer exceeds screen capacity, remove the oldest entry
    if (textBuffer.size() > MAX_LINES) {
        textBuffer.erase(textBuffer.begin());
    }

    // 3. Refresh display area
    display->clear();

    draw_background(display);

    // 4. Draw all lines currently in the buffer
    for (size_t i = 0; i < textBuffer.size(); ++i) {
        pico_ssd1306::drawText(
            display, font_5x8, 
            textBuffer[i].c_str(), 
            1, START_Y + (i * LINE_HEIGHT)
        );
    }

    display->sendBuffer();
}